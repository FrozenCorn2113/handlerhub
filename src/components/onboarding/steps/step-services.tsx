export function StepServices({ value, onChange }: StepServicesProps) {
  function toggleService(service: string) {
    if (value.includes(service)) {
      onChange(value.filter((s) => s !== service))
    } else {
      onChange([...value, service])
    }
  }

  return (
    <StepShell
      phase="What You Do"
      question="What services do you offer?"
      subtitle="Select all that apply."
    >
      <div className="grid grid-cols-2 gap-3">
        {SERVICES.map((service) => {
          const selected = value.includes(service)
          return (
            <button
              key={service}
              type="button"
              onClick={() => toggleService(service)}
              className={`relative flex flex-col items-center gap-2 rounded-xl border-2 p-4 text-center transition-all ${
                selected
                  ? 'border-paddock-green bg-paddock-green shadow-sm'
                  : 'border-sand bg-white hover:border-paddock-green'
              }`}
            >
              {/* Checkmark for selected */}
              {selected && (
                <div className="absolute right-2 top-2 flex size-5 items-center justify-center rounded-full bg-white/20">
                  <svg
                    className="size-3 text-white"
                    fill="none"
                    viewBox="0 0 24 24"
                    stroke="currentColor"
                    strokeWidth={3}
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M5 13l4 4L19 7"
                    />
                  </svg>
                </div>
              )}
              <div
                className={`transition-colors ${
                  selected ? 'text-white' : 'text-warm-gray'
                }`}
              >
                {SERVICE_ICONS[service] ?? (
                  <div className="size-7 rounded-full bg-sand" />
                )}
              </div>
              <span
                className={`text-xs font-medium leading-tight ${
                  selected ? 'text-white' : 'text-warm-gray'
                }`}
                style={{ fontFamily: 'var(--font-body)' }}
              >
                {service}
              </span>
            </button>
          )
        })}
      </div>
    </StepShell>
  )
}
